    public void CargarTabla(){
         ///llama la funcion limpiar tabla para evitar redundancia de datos.
        CleanTable();
            try {
                //reitera la conexion nuevamente para mostrar los datos
               String url = "jdbc:mariadb://localhost:3306/inventario";
               String usuario = "root";
               String pass = "root";
               Connection cnew2;
               Class.forName("org.mariadb.jdbc.Driver");
               //conexion getCon.. url+usuario+contrase√±a
               cnew2 = (Connection) DriverManager.getConnection(url,usuario,pass);
                org.mariadb.jdbc.Statement st = cnew2.createStatement();
                //sentencia sql
                String sql = "SELECT * FROM ventas";
                ResultSet rs = st.executeQuery(sql);
                //establece cada valor en cada opcion
                while(rs.next()){
                   String id = String.valueOf(rs.getInt("id_venta"));
                   String nombre_prod = rs.getString("nombre_del_prodvendido");
                   String id_prod = String.valueOf(rs.getInt("id_prod"));
                   String precio_prod = String.valueOf(rs.getInt("precio_prod"));
                   String total = String.valueOf(rs.getInt("total_venta"));
                   String cantidadprod = String.valueOf(rs.getInt("Cantidad_del_producto"));

                   String tabladb [] = {id,nombre_prod,precio_prod,total,cantidadprod};
                   DefaultTableModel tableModel =(DefaultTableModel)table_vtns.getModel();
                    //agrega los datos
                    //agrega las columnas a funcion de las columnas
           tableModel.addRow(tabladb);
                }
               
                //System.out.println("a");
            } catch (ClassNotFoundException | SQLException e) {
                System.err.println(e);
        }
        
        
    }
    
